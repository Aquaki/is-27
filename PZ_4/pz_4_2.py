# Дано целое число N (> 0). Найти сумму 11 + 22 + ... + NN
# Ввод данных с обработкой исключений
try:
    # Запрашиваем у пользователя ввод целого числа N
    n = int(input("Введите целое число N (> 0): "))

    # Проверка, что N больше 0
    if n <= 0:
        print("Ошибка: N должно быть больше 0.")
    else:
        # Инициализация переменной для хранения суммы
        total_sum = 0
        i = 1  # Инициализация переменной i, с которой начнем вычисления

        # Цикл для вычисления суммы 1^1 + 2^2 + ... + N^N
        while i <= n:
            # Инициализация переменной power для хранения значения i^i
            power = 1
            j = 1  # Инициализация переменной j для подсчета степени

            # Цикл для вычисления i^i
            while j <= i:
                power *= i  # Умножаем i на себя j раз
                j += 1  # Увеличиваем j на 1

            # Добавляем i^i к общей сумме
            total_sum += power
            i += 1  # Увеличиваем i на 1 для следующей итерации

        # Вывод результата
        print(f"Сумма 1^1 + 2^2 + ... + {n}^{n} для N={n} равна: {total_sum}")

# Обработка исключений на случай некорректного ввода
except ValueError:
    print("Ошибка: Пожалуйста, введите корректное целое число.")
