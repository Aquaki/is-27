# Дан целочисленный список размера N. Найти максимальное количество его одинаковых элементов.

def count_max_occurrences():
    while True:
        try:
            N = int(input("Введите размер списка: "))
            # Проверка, что размер списка положительный
            if N <= 0:
                raise ValueError("Размер списка должен быть положительным числом.")

            # Ввод элементов списка через пробел и преобразование их в целые числа
            my_list = list(map(int, input(f"Введите {N} чисел через пробел: ").split()))

            # Проверка, что введено ровно N чисел
            if len(my_list) != N:
                raise ValueError(f"Вы должны ввести ровно {N} чисел.")

            # Подсчет количества каждого элемента в списке
            element_count = {}
            for num in my_list:
                # Если элемент уже в словаре, увеличиваем его счетчик
                if num in element_count:
                    element_count[num] += 1
                else:
                    # Если элемента нет, добавляем его со счетчиком 1
                    element_count[num] = 1

            # Поиск максимального количества одинаковых элементов
            max_count = max(element_count.values())
            print(f"Максимальное количество одинаковых элементов: {max_count}")
            break  # Выход из цикла при успешном выполнении

        except ValueError as e:
            # Обработка ошибок ввода данных
            print(f"Ошибка: {e}. Пожалуйста, попробуйте еще раз.")

# Вызов функции
count_max_occurrences()
