# Используя словарь посчитать количество уникальных слов в заданном
# предложении «Изучаем язык Питон». Вывести на экран каждую пару
# «ключ:значение»

sentence = "Изучаем язык Питон"

words = sentence.split() # Разбиваем предложение на отдельные слова

word_count = {} # Создаем пустой словарь для хранения количества слов

# Проходим по всем словам и считаем их количество
for word in words:
    if word not in word_count:
        # Если слово еще не встречалось, добавляем его в словарь с количеством 1
        word_count[word] = 1
    else:
        # Если слово уже есть в словаре, увеличиваем на 1
        word_count[word] += 1

# Выводим каждую пару "ключ:значение" на экран
for key, value in word_count.items():
    print(f"{key}: {value}")

